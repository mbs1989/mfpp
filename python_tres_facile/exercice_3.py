"""
Ecrire un programme en Python qui demande à l'utilisateur
de saisir deux nombres a et b et de lui afficher leur maximum.
"""

a = int(input("saisissez le premier chiffre "))
b = int(input("saisissez le deuxième chiffre "))

if a > b:
    print("le maximim est ", a)
else:
    print("le maximum est", b)



